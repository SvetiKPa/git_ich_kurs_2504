# Напишите программу, которая преобразует список оценок по системе от 1 до 5 в текстовое представление. Нужно сохранить
# в списках числовой результат и текстовое представление. Где, 5 — "отлично", 3-4 — "хорошо", а 2 и ниже — "неудовлетворительно".
